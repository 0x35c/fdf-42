#include "fdf.h"

void	draw_line(t_points p1, t_points p2)
{
	float	m;
	float	e;

	m = abs(p2.y - p1.y)/abs(p2.x - p1.x);
	e = 0;
	while (p1.x <= p2.x)
	{
		ft_mlx_pixel_put(&img, p1.x, p1.y, 0x0000FF00);
		e-=m;
		if (e < -0.5)
		{
			p1.y--;
			e+=1.0;
		}
		p1.x++;
	}
}
